# Math module testing
import math

# Constants
print("Pi:", math.pi)
print("e:", math.e)
print("NaN:", math.nan)
print("Infinity:", math.inf)
print("Negative Infinity:", -math.inf)

# Trigonometry
direction = math.cos(math.pi/4)
print(direction)

# Ceiling and Floor
print("Ceiling is", math.ceil(10.3))
print("Floor is", math.floor(15.5))